function soma(a,b) {
    return a + b;
}

function subtracao(a,b) {
    return a - b;
}

function multiplicacao(a,b) {
    return a * b;
}

function divisao(a,b) {
    return a / b;
}

function modulacao(a,b) {
    return a % b;
}

function maiorNumero(x,y) {
    if (x > y) {
        return x;
    } return y;
}

function maiorDosNumeros(x,y,z) {
    if (x > y && x > z) {
        return x
    } if (y > x && y > z) {
        return y
    } return z
}

function positiveNegative(x) {
    if (x > 0) {
        return 'positive'
    } if (x < 0) {
        return 'negative'
    } return 'zero'
}

function triAngles(x,y,z) {
    if (x + y + z === 180 ) {
      return 'true'
    } return 'false'
}



